def _sort_key(item):
    k, v = item
    return (v["realm"], DIFFICULTY_ORDER.get(v.get("difficulty", "易"), 0))


def maps_for_realm(realm: int):
    """返回当前境界可进入的全部地图（含低阶），按境界+难度排序。"""
    return sorted(((k, v) for k, v in MAPS.items() if v["realm"] <= realm), key=_sort_key)


def maps_at_realm(realm: int):
    """仅返回该大境界本档的三张图（易→中→难）。"""
    return sorted(((k, v) for k, v in MAPS.items() if v["realm"] == realm), key=_sort_key)


def lower_maps(realm: int):
    """低于当前境界的历练图（折叠入口用）。"""
    return sorted(((k, v) for k, v in MAPS.items() if v["realm"] < realm), key=_sort_key)
